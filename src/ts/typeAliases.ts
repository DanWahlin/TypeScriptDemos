type int = number;
type float = number;
type StringArray = string[];
type NumberArray = number[];

var x: int = 5;
var lat: float = 120.05;
var names: StringArray = ['Ted', 'Michelle'];
var ages: NumberArray = [35, 55, 83];

console.log('');
console.log('type alias x : int = ' + x);
console.log('type alias lat : float = ' + lat);
console.log('type alias names : StringArray = ' + names);
console.log('type alias ages : NumberArray = ' + ages);